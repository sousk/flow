class EntriesController < ApplicationController

  def index
    period = case
      when params[:mm] then 'month'
      when params[:yyyy] then 'year'
      else nil end
    pnum = params[:page] || 1
    
    @entries = if period
      time = time_of params
      Entry.list.between(
        time.send("beginning_of_#{period}"), time.send("end_of_#{period}")
      ).paginate :page=>pnum, :per_page=>Entry.per_page
    else
      Entry.list.paginate(:page => pnum, :per_page => Entry.per_page)
    end
    
    respond_to do |format|
      format.html
      format.xml  { render :xml => @entries }
    end
  end

  def show
  end
  
  def destroy
  end
  
  private
  def time_of(params)
    now = Time.now
    [
      params[:yyyy] || now.year,
      params[:mm] || now.month,
      params[:dd] || now.day
    ].join('-').to_time
  end
end
